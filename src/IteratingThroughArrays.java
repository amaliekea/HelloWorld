public class IteratingThroughArrays {
    public static void main(String[] args) {
        int[] arr = new int[]{92, 12, 38, 74}; //her har vi bestemt værdien af de forskellige index
        for (int i = 0; i < arr.length; i++) { //vi bliver ved med at iterere indtil længden af array
            System.out.println(arr[i]);
        }
        System.out.println();


    }
}
